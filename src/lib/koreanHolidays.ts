export function getHolidaysForMonth(year: number, month: number): HolidayMap {
  const mm = String(month).padStart(2, '0')
  const result: HolidayMap = {}

  for (const [mmdd, name] of FIXED) {
    if (mmdd.startsWith(mm)) {
      result[`${year}-${mmdd}`] = name
    }
  }

  const variable = VARIABLE[year] ?? []
  for (const [mmdd, name] of variable) {
    if (mmdd.startsWith(mm)) {
      const key = `${year}-${mmdd}`
      if (result[key]) {
        result[key] = result[key] + '·' + name
      } else {
        result[key] = name
      }
    }
  }

  return result
}
